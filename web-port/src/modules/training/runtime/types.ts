/**
 * ERB 변환 코드 런타임 타입 정의
 * 원본 ERB의 모든 변수를 TypeScript로 매핑
 */

/**
 * TrainingContext - 조교 실행 컨텍스트
 * ERB의 모든 전역 변수와 함수를 포함
 */
export interface TrainingContext {
  // ============================================================================
  // 기본 파라미터 (PALAM:n)
  // ============================================================================
  params: number[];           // PALAM:0~299 (쾌C, 쾌V, 쾌A, 쾌B, 윤활, 굴종, 욕정 등)

  // ============================================================================
  // 재능/소질 (TALENT:n)
  // ============================================================================
  talents: number[];          // TALENT:0~239 (처녀, 항문처녀, 키스미경험 등)
  playerTalents: number[];    // TALENT:PLAYER:n
  assiTalents: number[];      // TALENT:ASSI:n
  masterTalents: number[];    // TALENT:MASTER:n

  // ============================================================================
  // 플래그 (FLAG:n, TFLAG:n)
  // ============================================================================
  flags: number[];            // FLAG:0~299 (전역 플래그)

  // ============================================================================
  // 능력치 (ABL:n)
  // ============================================================================
  abilities: number[];        // ABL:0~299 (C감각, V감각, A감각, B감각, 입술, 혀 등)
  assiAbilities: number[];    // ABL:ASSI:n
  masterAbilities: number[];  // ABL:MASTER:n

  // ============================================================================
  // 경험치 (EXP:n)
  // ============================================================================
  exp: number[];              // EXP:0~299 (각종 경험치)
  playerExp: number[];        // EXP:PLAYER:n

  // ============================================================================
  // 구슬 (JUEL:n)
  // ============================================================================
  juel: number[];             // JUEL:0~299

  // ============================================================================
  // 문자열/임시 변수
  // ============================================================================
  saveStr: string[];          // SAVESTR:0~99 (문자열 저장)

  // ============================================================================
  // 아이템 (ITEM:n)
  // ============================================================================
  items: number[];            // ITEM:0~299 (아이템 보유 수량)

  // ============================================================================
  // 조교 관련
  // ============================================================================
  loseBase: number[];         // LOSEBASE:0~99 (기본 상실량)
  stain: number[];            // STAIN:0~99 (오염도 - 정액, 애액 등)
  playerStain: number[];      // STAIN:PLAYER:n
  masterStain: number[];      // STAIN:MASTER:n

  // ============================================================================
  // BASE 스탯
  // ============================================================================
  playerBase: number[];       // BASE:PLAYER:0~99
  masterBase: number[];       // BASE:MASTER:0~99

  // ============================================================================
  // 관계도 (RELATION)
  // ============================================================================
  relation: number[][];       // RELATION:n:m (캐릭터 간 관계)

  // ============================================================================
  // 조수/플레이어
  // ============================================================================
  assiPlay: number;           // ASSIPLAY (조수 플레이 모드)
  player: any;                // PLAYER
  assi: any;                  // 조수 객체

  // ============================================================================
  // 출력/UI 함수
  // ============================================================================
  showMessage(msg: string): void;
  showText(text: any): void;
  getString(key: string): string;

  print(text: string): void;
  printChar(char: string): void;
  printValue(value: any): void;
  printValueLine(value: any): void;

  drawLine(char?: string): void;
  waitInput(): void;

  addButton(label: string, value: number): void;

  // ============================================================================
  // 색상/스타일
  // ============================================================================
  setColor(color: number): void;
  resetColor(): void;
  setMultiColor(...colors: number[]): void;
  getColor(): number;

  setAlignment(align: string): void;
  setFontStyle(style: number): void;
  resetFontStyle(): void;
  setFontBold(bold: boolean): void;
  setFontItalic(italic: boolean): void;
  setFontRegular(): void;

  // ============================================================================
  // 배율/계산
  // ============================================================================
  times(target: string, multiplier: number): void;

  // ============================================================================
  // 입출력
  // ============================================================================
  wait(): Promise<void>;
  clearLine(count: number): void;
  printPlain(text: string): void;
  reuseLastLine(): void;
  printHtml(html: string): void;
  split(ratio: number): void;
  skipDisplay(flag: boolean): void;

  // ============================================================================
  // 그래픽
  // ============================================================================
  drawBar(current: number, max: number, length: number): void;
  drawBarStr(...args: any[]): void;

  // ============================================================================
  // 입력
  // ============================================================================
  inputNumber(): Promise<number>;
  inputString(): Promise<string>;
  oneInput(): Promise<number>;
  oneInputString(): Promise<string>;
  timedInput(timeout: number): Promise<number>;
  timedWait(time: number): Promise<void>;

  // ============================================================================
  // 능력치 체크
  // ============================================================================
  checkAbilityUp(abl: number): void;
  checkCharacterAbilityUp(abl: number): void;

  // ============================================================================
  // 배열/변수 조작
  // ============================================================================
  varSet(...args: any[]): void;
  arraySet(...args: any[]): void;
  arrayShift(...args: any[]): void;
  arrayCopy(...args: any[]): void;
  randomize(): void;
  swap(...args: any[]): void;

  // ============================================================================
  // 게임 제어
  // ============================================================================
  quitGame(): void;
  restart(): void;
  saveGame(): Promise<void>;
  loadGame(): Promise<void>;
  deleteData(slot: number): void;
  resetData(): void;

  // ============================================================================
  // 비트 연산
  // ============================================================================
  getBit(...args: any[]): number;
  setBit(...args: any[]): void;
  clearBit(...args: any[]): void;
  invertBit(...args: any[]): void;

  // ============================================================================
  // 출력 제어
  // ============================================================================
  setNoSkip(flag: boolean): void;
  putForm(text: string): void;

  // ============================================================================
  // 문자열 함수
  // ============================================================================
  getStrData(key: string): string;
  replace(...args: any[]): string;
  substring(...args: any[]): string;
  strFind(...args: any[]): number;
  strCount(...args: any[]): number;
  strLen(str: string): number;
  isNumeric(str: string): boolean;
  toUpper(str: string): string;
  toLower(str: string): string;
  toFull(str: string): string;
  toHalf(str: string): string;
  toNumber(str: string): number;
  toString(val: any): string;
  formatNumber(num: number): string;
  escape(str: string): string;
  encodeToUni(...args: any[]): string;

  // ============================================================================
  // 수학 함수
  // ============================================================================
  limit(val: number, min: number, max: number): number;
  inRange(...args: any[]): boolean;

  // ============================================================================
  // 배열 함수
  // ============================================================================
  sumArray(...args: any[]): number;
  maxArray(...args: any[]): number;
  minArray(...args: any[]): number;
  match(...args: any[]): number;
  groupMatch(...args: any[]): boolean;
  removeDuplicates(...args: any[]): any[];
  sumConditionalArray(...args: any[]): number;
  arraySize(...args: any[]): number;

  // ============================================================================
  // 시간
  // ============================================================================
  getTime(): number;

  // ============================================================================
  // 관계도 함수
  // ============================================================================
  getRelation(character: any, type: string): number;
  setRelation(character: any, type: string, value: number): void;
}

/**
 * Character - 캐릭터 객체
 * ERB의 캐릭터별 변수를 포함
 */
export interface Character {
  // ============================================================================
  // SOURCE - 파라미터 변화량
  // ============================================================================
  source: number[];           // SOURCE:0~299 (PALAM 증가량)

  // ============================================================================
  // 캐릭터 플래그 (CFLAG:n)
  // ============================================================================
  cflags: number[];           // CFLAG:0~299 (캐릭터 고유 플래그)
  flags: number[];            // 별칭

  // ============================================================================
  // 임시 플래그 (TFLAG:n)
  // ============================================================================
  tflags: number[];           // TFLAG:0~99 (조교 중 임시 플래그)

  // ============================================================================
  // 장비 (TEQUIP:n)
  // ============================================================================
  equipment: number[];        // TEQUIP:0~299 (장비 상태)

  // ============================================================================
  // 각인 (MARK:n)
  // ============================================================================
  mark: number[];             // MARK:0~99

  // ============================================================================
  // 임시 변수 (TCVAR:n)
  // ============================================================================
  cvar: number[];             // TCVAR:0~99 (캐릭터 임시 변수)
  tcvar: number[];            // 별칭

  // ============================================================================
  // 기타
  // ============================================================================
  id: number;
  name: string;
}
